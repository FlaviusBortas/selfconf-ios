import Foundation
import UIKit

extension UIColor {
    
    @objc(SC_teal)
    static let scTeal  = UIColor(named: "SC_teal")!
    
    @objc(SC_darkTeal)
    static let scDarkTeal  = UIColor(named: "SC_darkTeal")!
    
    @objc(SC_offWhite)
    static let scOffWhite = UIColor(named: "SC_offWhite")!
    
    @objc(SC_orange)
    static let scOrange = UIColor(named: "SC_orange")!
    
    @objc(SC_purple)
    static let scPurple = UIColor(named: "SC_purple")!
    
    @objc(SC_red)
    static let scRed = UIColor(named: "SC_red")!
    
    @objc(SC_yellow)
    static let scYellow = UIColor(named: "SC_yellow")!
    
}
